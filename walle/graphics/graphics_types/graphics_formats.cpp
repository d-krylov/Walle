bool IsDepthFormat(Format format) {}

bool IsStencilFormat(Format format) {}

std::size_t GetFormatComponentCount(Format format) { return format_information[format].components.size(); }

std::size_t GetFormatDepthSize(Format format) {
  const auto &components = format_information[format].components;
  auto depth_component = std::ranges::find(components, 'D', &FormatComponent::name);
  return depth_component == components.end() ? 0 : depth_component->bits;
}

std::size_t GetFormatStencilSize(Format format) {
  const auto &components = format_information[format].components;
  auto stencil_component = std::ranges::find(components, 'S', &FormatComponent::name);
  return stencil_component == components.end() ? 0 : stencil_component->bits;
}

std::size_t GetFormatElementSizeWithAspect(Format format, ImageAspectMask aspect_mask) {
  if (aspect_mask & ImageAspectMaskBit::STENCIL_BIT) return GetFormatStencilSize(format) / 8;
  if (aspect_mask & ImageAspectMaskBit::DEPTH_BIT) return GetFormatDepthSize(format) / 8;

  return format_information[format].block_size;
}

std::size_t GetFormatElementSize(Format format) { return GetFormatElementSizeWithAspect(format, ImageAspectMaskBit::COLOR_BIT); }

